package it.csi.iuffi.iuffiweb.presentation.quadro.interventi.base;

public class ParametriRicercaParticelle
{
  protected String istatProvincia;
  protected String istatComune;
  protected String sezione;
  protected String foglio;
  protected String particella;
  protected String subalterno;

  public String getIstatComune()
  {
    return istatComune;
  }

  public void setIstatComune(String istatComune)
  {
    this.istatComune = istatComune;
  }

  public String getSezione()
  {
    return sezione;
  }

  public void setSezione(String sezione)
  {
    this.sezione = sezione;
  }

  public String getFoglio()
  {
    return foglio;
  }

  public void setFoglio(String foglio)
  {
    this.foglio = foglio;
  }

  public String getParticella()
  {
    return particella;
  }

  public void setParticella(String particella)
  {
    this.particella = particella;
  }

  public String getSubalterno()
  {
    return subalterno;
  }

  public void setSubalterno(String subalterno)
  {
    this.subalterno = subalterno;
  }

  public String getIstatProvincia()
  {
    return istatProvincia;
  }

  public void setIstatProvincia(String istatProvincia)
  {
    this.istatProvincia = istatProvincia;
  }
}