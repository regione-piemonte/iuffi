package it.csi.iuffi.iuffiweb.integration.ws.regata;

public class DatiAziendaDTO {
	protected String cuaa;
	protected String dataFineEsercizio;
	protected String dataConcessione;
	protected String dataInizioAnalisi;
	protected String checkDeminimis;
	protected String checkAiuti;
	protected String checkDeggendorf;
	
	public String getCuaa() {
		return cuaa;
	}
	public void setCuaa(String cuaa) {
		this.cuaa = cuaa;
	}
	public String getDataFineEsercizio() {
		return dataFineEsercizio;
	}
	public void setDataFineEsercizio(String dataFineEsercizio) {
		this.dataFineEsercizio = dataFineEsercizio;
	}
	public String getDataConcessione() {
		return dataConcessione;
	}
	public void setDataConcessione(String dataConcessione) {
		this.dataConcessione = dataConcessione;
	}
	public String getDataInizioAnalisi() {
		return dataInizioAnalisi;
	}
	public void setDataInizioAnalisi(String dataInizioAnalisi) {
		this.dataInizioAnalisi = dataInizioAnalisi;
	}
	public String getCheckDeminimis() {
		return checkDeminimis;
	}
	public void setCheckDeminimis(String checkDeminimis) {
		this.checkDeminimis = checkDeminimis;
	}
	public String getCheckAiuti() {
		return checkAiuti;
	}
	public void setCheckAiuti(String checkAiuti) {
		this.checkAiuti = checkAiuti;
	}
	public String getCheckDeggendorf() {
		return checkDeggendorf;
	}
	public void setCheckDeggendorf(String checkDeggendorf) {
		this.checkDeggendorf = checkDeggendorf;
	}
}
