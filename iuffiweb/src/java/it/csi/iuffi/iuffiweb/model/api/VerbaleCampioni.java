package it.csi.iuffi.iuffiweb.model.api;

public class VerbaleCampioni
{
  
  private Integer idCampionamento;
  private String organismiNociviCamp;
  private Integer nCampioni;
  private String codiceCampioni;
  private String tipologiaCampione;
  private String note;
  
  public Integer getIdCampionamento()
  {
    return idCampionamento;
  }
  public void setIdCampionamento(Integer idCampionamento)
  {
    this.idCampionamento = idCampionamento;
  }
  public String getOrganismiNociviCamp()
  {
    return organismiNociviCamp;
  }
  public void setOrganismiNociviCamp(String organismiNociviCamp)
  {
    this.organismiNociviCamp = organismiNociviCamp;
  }
  public Integer getnCampioni()
  {
    return nCampioni;
  }
  public void setnCampioni(Integer nCampioni)
  {
    this.nCampioni = nCampioni;
  }
  public String getCodiceCampioni()
  {
    return codiceCampioni;
  }
  public void setCodiceCampioni(String codiceCampioni)
  {
    this.codiceCampioni = codiceCampioni;
  }
  public String getTipologiaCampione()
  {
    return tipologiaCampione;
  }
  public void setTipologiaCampione(String tipologiaCampione)
  {
    this.tipologiaCampione = tipologiaCampione;
  }
  public String getNote()
  {
    return note;
  }
  public void setNote(String note)
  {
    this.note = note;
  }

}
